# ^Operations >N

def print_items(n):
    #runs for n times
    for i in range(n):
        print(i)
    
    #runs for n times    
    for j in range(n):
        print(j)

#DROP CONSTANTS: n+n=2n=n
print_items(10) 



           
              
                  
                  






